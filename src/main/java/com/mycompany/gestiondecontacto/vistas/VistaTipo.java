    private int obtenerSiguienteCodigoDisponoble(ArrayList<Tipo> tipos) {
        int maxCodigo = 0;
        for (Tipo t : tipos) {
            if (t.getCodigo() > maxCodigo) {
                maxCodigo = t.getCodigo();
            }
        }
        return maxCodigo + 1;
    }

    public void mostrarDatos() {
        modelo.setNumRows(tipos.size());
        for (int i = 0; i < tipos.size(); i++) {
            modelo.setValueAt(tipos.get(i).getCodigo(), i, 0);
            modelo.setValueAt(tipos.get(i).getNombre(), i, 1);
        }
        jtDatos.setModel(modelo);
    }

    private void jtDatosMouseClicked(java.awt.event.MouseEvent evt) {
        // Obtiene el índice de la fila seleccionada
        int filaSeleccionada = jtDatos.getSelectedRow();

        if (filaSeleccionada >= 0) {
            // Obtiene el objeto Tipo completo de la lista `tipos`
            Tipo tipoSeleccionado = this.tipos.get(filaSeleccionada);

            // Muestra el nombre (tipo) en el campo de texto
            tfTipo.setText(tipoSeleccionado.getNombre());
        }
    }

