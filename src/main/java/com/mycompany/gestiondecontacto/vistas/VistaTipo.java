    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
        if (jtDatos.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "SELECIONE EL REGISTRO PARA ELIMINAR");

        } else {
            this.tipos.remove(this.jtDatos.getSelectedRow());
            this.tipoManager.guardarTipos(this.tipos);
            this.mostrarDatos();
            JOptionPane.showMessageDialog(null, "Tipo eliminado con exito");
        }
    }//GEN-LAST:event_button1ActionPerformed

    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        // TODO add your handling code here:
        String nuevoNombre = tfTipo.getText();
        int filaSeleccionada = jtDatos.getSelectedRow();

        if (nuevoNombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "INGRESE LOS DATOS");
            return; // Sale del método si el campo está vacío
        }

        if (filaSeleccionada >= 0) {
            // Lógica de MODIFICACIÓN
            Tipo t = this.tipos.get(filaSeleccionada);
            t.setNombre(nuevoNombre);

            JOptionPane.showMessageDialog(null, "Tipo modificado con éxito");

        } else {
            // Lógica de AÑADIR NUEVO
            Tipo t = new Tipo();
            t.setCodigo(codigo);
            codigo++;
            t.setNombre(nuevoNombre);
            this.tipos.add(t);

            JOptionPane.showMessageDialog(null, "Tipo añadido con éxito");
        }

        // Limpia el campo de texto y actualiza la tabla
        tfTipo.setText("");
        mostrarDatos();

        // **OPCIONAL PERO RECOMENDADO:** Reiniciar la selección de la tabla
        jtDatos.clearSelection();
        this.tipoManager.guardarTipos(this.tipos);
    }//GEN-LAST:event_button2ActionPerformed

