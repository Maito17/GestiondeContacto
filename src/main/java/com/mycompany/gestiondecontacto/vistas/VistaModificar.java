    public ImageIcon scaleImage(ImageIcon icon, int w, int h) {
        Image img = icon.getImage();
        Image newimg = img.getScaledInstance(w, h, java.awt.Image.SCALE_SMOOTH);
        return new ImageIcon(newimg);

    }

    public VistaModificar(Contacto c, VistasContacto vc) {
        initComponents();
        int width = 80;
        int height = 80;
        ImageIcon originalIcon = new ImageIcon(getClass().getResource("/img/la-configuracion-de-privacidad.png")); 

        if (originalIcon.getImage() != null) {
            ImageIcon scaledIcon = scaleImage(originalIcon, width, height);
            // Suponiendo que el JLabel donde va la imagen se llama 'jLabel1'
            // Â¡CAMBIA 'jLabel1' POR EL NOMBRE REAL DE TU COMPONENTE DE IMAGEN!
            jLabel1.setIcon(scaledIcon);
            jLabel1.setText(""); // Opcional: Eliminar cualquier texto por defecto del JLabel
        } else {
            System.err.println("Error: No se pudo cargar la imagen para VistaModificar.");
        }
        this.c = c;
        this.vc = vc;
        vc.setVisible(false);
        jlCodigo.setText(String.valueOf(c.getCodigo()));
        tfNombre.setText(c.getNombre());
        tfApellido.setText(c.getApellido());
    }

